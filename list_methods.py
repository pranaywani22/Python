#list methods
l1=[1,2,5,4,8,45,76,23,9,65]
print(l1)
l1.sort() #this updates the list in order
print(l1)
l1.reverse() #this reverse the list
print(l1)
l1.append(10) #this add an element 10 in the end
print(l1)
l1.sort()
print(l1)
l1.insert(0,560)    #this inserts 560 in 0th index
print(l1)  
l1.pop(5)   #this deletes the number at index 5
print(l1)
l1.remove(23)   #this deletes 23 from the list
print(l1)